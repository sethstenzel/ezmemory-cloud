# Abuse / storage protection
#
# ezmemory is a text-only service: there are no file-upload endpoints, no
# MEDIA_ROOT, and no form anywhere accepts a file. BlockUploadsMiddleware
# rejects every multipart/form-data request (the only way to send files)
# before it is parsed. The caps below bound how much text a single account
# (or a single request) can store.

# Reject any request body larger than 256 KB — orders of magnitude above any
# legitimate note edit, far below anything that could smuggle in media blobs.
DATA_UPLOAD_MAX_MEMORY_SIZE = 256 * 1024

# Per-account content quotas (enforced in views).
