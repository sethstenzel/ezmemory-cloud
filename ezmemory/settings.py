EZMEMORY_MAX_NODES_PER_USER = 10_000       # bullets across all documents
EZMEMORY_MAX_DOCUMENTS_PER_USER = 1_000
EZMEMORY_MAX_FOLDERS_PER_USER = 100
EZMEMORY_MAX_DOC_TITLE_CHARS = 200

# Signup throttling: max new accounts per source IP per hour.
